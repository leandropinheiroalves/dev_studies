"""Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário."""

lado_quadrado = float(input('Informe o diâmetro de um dos lados do quadrado: '))
area_quadrado = lado_quadrado * lado_quadrado

print(f'A área de um quadrado de lado  {lado_quadrado} é: {area_quadrado} e o dobro dessa área é: {area_quadrado*2}')